/**
*_strpbrk - locate the first occurrence in a string
*@s: source
*@accept: searched in source
*Return: pointer to the first occurrence
*/
char *_strpbrk(char *s, char *accept)
{
	int i, j;

	for (i = 0; s[i] != '\0'; i++)
	{
		for (j = 0; accept[j] != '\0'; j++)
		{
			if (s[i] == accept[j])
				return (&s[i]);
		}
	}
	return (0);
}
